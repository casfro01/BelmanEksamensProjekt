package dk.eksamensprojekt.belmaneksamensprojekt.BE;

public class BaseUser {
    private int id;
    private String name;

    public BaseUser(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
